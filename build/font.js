const fs = require('fs');
const path = require('path');

const fontMap = {
  headphones: '\\e910',
};
const fontRootName = 'zm-icon';
const outputPath = path.resolve(__dirname, '../src/style/font.css');

let fontCss = `/* Automatically generated by '/build/font.js */

@font-face {
  font-family: 'element-icons';
  src: url('../assets/fonts/test.woff') format('woff'), /* chrome, firefox */
       url('../assets/fonts/test.ttf') format('truetype'); /* chrome, firefox, opera, Safari, Android, iOS 4.2+*/
  font-weight: normal;
  font-display: inline-block;
  font-style: normal;
}

[class^="${fontRootName}-"], [class*=" ${fontRootName}-"] {
  /* use !important to prevent issues with browser extensions that change fonts */
  font-family: 'element-icons' !important;
  speak: none;
  font-style: normal;
  font-weight: normal;
  font-variant: normal;
  text-transform: none;
  line-height: 1;
  vertical-align: baseline;
  display: inline-block;

  /* Better Font Rendering =========== */
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}
`;
Object.keys(fontMap).forEach((fontName) => {
  fontCss += `
.${fontRootName}-${fontName}:before {
  content: "${fontMap[fontName]}";
}
`;
});

if (fs.existsSync(outputPath)) {
  fs.writeFileSync(outputPath, fontCss);
} else {
  fs.appendFileSync(outputPath, fontCss);
}
